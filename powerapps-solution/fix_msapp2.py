# ── App-Control JSON (Controls\1.json) mit OnStart ───────────────────────────

def build_app_json(original_app_json_bytes):
    """Nimm original Controls/1.json und füge OnStart in App-Rules ein."""
    original = json.loads(original_app_json_bytes.decode("utf-8"))
    # OnStart in Rules des App-Controls einfügen
    app_rules = original["TopParent"]["Rules"]
    # Entferne altes OnStart falls vorhanden
    app_rules = [r for r in app_rules if r["Property"] != "OnStart"]
    app_rules.append({
        "Property": "OnStart",
        "Category": "Behavior",
        "InvariantScript": ONSTART,
        "RuleProviderType": "Unknown"
    })
    original["TopParent"]["Rules"] = app_rules
    prop_state = original["TopParent"].get("ControlPropertyState", [])
    if "OnStart" not in prop_state:
        prop_state.append("OnStart")
    original["TopParent"]["ControlPropertyState"] = prop_state
    return json.dumps(original, indent=2).encode("utf-8")


# ── Packing ───────────────────────────────────────────────────────────────────

def build():
    print(f"Quelle:  {SOLUTION_IN}")
    print(f"Ausgabe: {SOLUTION_OUT}\n")

    with zipfile.ZipFile(SOLUTION_IN) as sol:
        orig_msapp = sol.read(CANVAS_PATH)

    new_msapp_buf = io.BytesIO()
    with zipfile.ZipFile(io.BytesIO(orig_msapp)) as orig:
        orig_app_json = None
        for item in orig.infolist():
            if item.filename.replace("\\", "/") == "Controls/1.json":
                orig_app_json = orig.read(item.filename)
                break

        with zipfile.ZipFile(new_msapp_buf, "w", zipfile.ZIP_DEFLATED) as new:
            for item in orig.infolist():
                fn_norm = item.filename.replace("\\", "/")

                if fn_norm == "Controls/1.json":
                    data = build_app_json(orig_app_json)
                    new.writestr(item, data)
                    print(f"  ✓ {item.filename}  (OnStart hinzugefügt)")

                elif fn_norm == "Src/App.pa.yaml":
                    new.writestr(item, APP_YAML.encode("utf-8"))
                    print(f"  ✓ {item.filename}  (minimal)")

                elif fn_norm == "Src/_EditorState.pa.yaml":
                    new.writestr(item, EDITOR_YAML.encode("utf-8"))
                    print(f"  ✓ {item.filename}  (Screens aktualisiert)")

                elif fn_norm == "Src/Screen1.pa.yaml":
                    new.writestr(item, SCREEN1_YAML.encode("utf-8"))
                    print(f"  ✓ {item.filename}  (unverändert)")

                else:
                    new.writestr(item, orig.read(item.filename))
                    print(f"  ✓ {item.filename}")

            # Neue Screens als Controls-JSON (Backslash-Pfade)
            screens = [
                ("Controls\\5.json", "DispatchScreen", 5, 1, build_dispatch(50)),
                ("Controls\\6.json", "ETAScreen",      6, 2, build_eta(100)),
                ("Controls\\7.json", "TaxiStatusScreen", 7, 3, build_status(150)),
            ]
            for path, name, uid, idx, children in screens:
                data = json.dumps(screen_json(name, uid, idx, children), indent=2)
                new.writestr(path, data.encode("utf-8"))
                print(f"  ✓ {path}  ({name}, {len(children)} Controls)")

    msapp_bytes = new_msapp_buf.getvalue()
    print(f"\nmsapp: {len(msapp_bytes):,} Bytes")

    out_buf = io.BytesIO()
    with zipfile.ZipFile(SOLUTION_IN) as sol:
        with zipfile.ZipFile(out_buf, "w", zipfile.ZIP_DEFLATED) as out:
            for item in sol.infolist():
                if item.filename == CANVAS_PATH:
                    out.writestr(item, msapp_bytes)
                    print(f"  ✓ {item.filename}  (ersetzt)")
                else:
                    out.writestr(item, sol.read(item.filename))

    out_buf.seek(0)
    with open(SOLUTION_OUT, "wb") as f:
        f.write(out_buf.read())

    kb = os.path.getsize(SOLUTION_OUT) // 1024
    print(f"\n✅ Fertig: {SOLUTION_OUT}  ({kb} KB)")
    print("\nImport: make.powerapps.com → Solutions → Petra → Importieren")
    print("→ Datei: Petra_CanvasApp_KEIN_FLOW.zip")


if __name__ == "__main__":
    build()
