def screen_json(name, uid, index, children):
    top = {
        "TopParent": ctrl_info(
            name, uid,
            "http://microsoft.com/appmagic/screen", "screen", "1.0",
            "defaultScreenStyle", "",
            [
                rule("Fill", "Design", "RGBA(245,247,250,1)"),
                rule("ImagePosition", "Design", "ImagePosition.Fit"),
                rule("Height", "Design", "Max(App.Height, App.MinScreenHeight)"),
                rule("Width", "Design", "Max(App.Width, App.MinScreenWidth)"),
                rule("Size", "Design", "1 + CountRows(App.SizeBreakpoints) - CountIf(App.SizeBreakpoints, Value >= Self.Width)"),
                rule("Orientation", "Design", "If(Self.Width < Self.Height, Layout.Vertical, Layout.Horizontal)"),
                rule("LoadingSpinner", "Design", "LoadingSpinner.None"),
                rule("LoadingSpinnerColor", "Design", "RGBA(56,96,178,1)"),
            ],
            children
        )
    }
    # Fix parent names
    top["TopParent"]["IsDataControl"] = False
    return top


