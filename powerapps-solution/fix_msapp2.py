def ctrl_info(name, uid, template_id, template_name, template_ver,
              style, parent, rules, children=None, is_data=False):
    return {
        "Type": "ControlInfo",
        "Name": name,
        "HasDynamicProperties": False,
        "Template": {
            "Id": template_id,
            "Version": template_ver,
            "LastModifiedTimestamp": "0",
            "Name": template_name,
            "FirstParty": True,
            "IsPremiumPcfControl": False,
            "IsCustomGroupControlTemplate": False,
            "CustomGroupControlTemplateName": "",
            "IsComponentDefinition": False,
            "OverridableProperties": {}
        },
        "Index": uid,
        "PublishOrderIndex": uid,
        "VariantName": "",
        "LayoutName": "",
        "MetaDataIDKey": "",
        "PersistMetaDataIDKey": False,
        "IsFromScreenLayout": False,
        "StyleName": style,
        "Parent": parent,
        "IsDataControl": is_data,
        "AllowAccessToGlobals": True,
        "OptimizeForDevices": "Off",
        "IsGroupControl": False,
        "IsAutoGenerated": False,
        "Rules": rules,
        "ControlPropertyState": [r["Property"] for r in rules],
        "IsLocked": False,
        "ControlUniqueId": str(uid),
        "Children": children or []
    }


def rule(prop, cat, script):
    return {"Property": prop, "Category": cat,
            "InvariantScript": script, "RuleProviderType": "Unknown"}


def label(name, uid, parent, x, y, w, h, text, size=14,
          bold=False, color="RGBA(50,50,50,1)", fill="RGBA(0,0,0,0)",
          align="Align.Left", valign="VerticalAlign.Middle"):
    rules = [
        rule("X", "Design", str(x)),
        rule("Y", "Design", str(y)),
        rule("Width", "Design", str(w)),
        rule("Height", "Design", str(h)),
        rule("Text", "Data", text),
        rule("Size", "Design", str(size)),
        rule("Color", "Design", color),
        rule("Fill", "Design", fill),
        rule("Align", "Design", align),
        rule("VerticalAlign", "Design", valign),
    ]
    if bold:
        rules.append(rule("FontWeight", "Design", "FontWeight.Bold"))
    return ctrl_info(name, uid,
        "http://microsoft.com/appmagic/label", "label", "2.9.1",
        "defaultLabelStyle", parent, rules)


